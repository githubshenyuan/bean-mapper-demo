public class FlatEntity {
    private String uuidSrc;
    private String userNameSrc;
    private String passwordSrc;
    private String uuidTar;
    private String userNameTar;
    private String passwordTar;

    public String getUuidSrc() {
        return uuidSrc;
    }

    public void setUuidSrc(String uuidSrc) {
        this.uuidSrc = uuidSrc;
    }

    public String getUserNameSrc() {
        return userNameSrc;
    }

    public void setUserNameSrc(String userNameSrc) {
        this.userNameSrc = userNameSrc;
    }

    public String getPasswordSrc() {
        return passwordSrc;
    }

    public void setPasswordSrc(String passwordSrc) {
        this.passwordSrc = passwordSrc;
    }

    public String getUuidTar() {
        return uuidTar;
    }

    public void setUuidTar(String uuidTar) {
        this.uuidTar = uuidTar;
    }

    public String getUserNameTar() {
        return userNameTar;
    }

    public void setUserNameTar(String userNameTar) {
        this.userNameTar = userNameTar;
    }

    public String getPasswordTar() {
        return passwordTar;
    }

    public void setPasswordTar(String passwordTar) {
        this.passwordTar = passwordTar;
    }
}
